
class Square2 {
    public static void printSquare(int x) {
        System.out.println(x*x);
    }
    public static void times(int a, int b) {
        System.out.println(a*b);
    }
    public static void main(String[] arguments) {
        printSquare(5);
        times(2, 3);
        times(5, 3);
    }
}